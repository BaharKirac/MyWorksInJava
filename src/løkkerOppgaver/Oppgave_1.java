/* Oppgave 1
Lag en forløkke som viser alle tallene fra 0 til 10 på
hver sin linje under hverandre (0 til og med 9). */
public class Oppgave_1 {
    public static void main(String[] args) {
        for (int i=10; i<=20; i++){
            System.out.println(i);
        }
    }
}

